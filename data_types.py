# Python Data Types - Basic Examples

# Integer
a = 10
print("Integer:", a)

# Float
b = 3.14
print("Float:", b)

# String
name = "Python"
print("String:", name)

# List
numbers = [1, 2, 3, 4]
print("List:", numbers)

# Tuple
coordinates = (10, 20)
print("Tuple:", coordinates)

# Set
unique_values = {1, 2, 3, 3}
print("Set:", unique_values)

# Dictionary
student = {
    "name": "Likhitha",
    "exam": "GATE",
    "subject": "Python"
}
print("Dictionary:", student)

# Bytes
byte_data = bytes([65, 66, 67])
print("Bytes:", byte_data)

# None
result = None
print("None datatype:", result)
